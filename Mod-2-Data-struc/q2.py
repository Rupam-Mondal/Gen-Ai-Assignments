"""
1.  It maintains the order of user input
"""

list1 = [1 , 2 , 3 , 4 , 5]
print(list1)

"""
2.  Indexed based access we can access any element in list
    by using index
"""
print(list1[2])

"""
3.  It is mutable, it means we can actually modify elements
    of a list
"""

list1[0] = "Rupam"
print(list1)


"""
4.  We can add different type of data type in a list
"""
a = [1 , 2.3 , "Rupam" , {3 , 4}]
print(a)